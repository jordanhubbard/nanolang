#pragma once

#include <math.h>

#define PT2_PI 3.1415926535897932384626433832795
#define PT2_2PI 6.283185307179586476925286766559

static inline double pt2_sqrt(double value) {
    return sqrt(value);
}

static inline double pt2_cos(double value) {
    return cos(value);
}

static inline double pt2_tan(double value) {
    return tan(value);
}
